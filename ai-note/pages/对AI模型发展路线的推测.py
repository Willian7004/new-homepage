st.write("思维链模型的换代在编程和数学竞赛等测试集中有提升，并且在不少测试集达到或超过人类水平，但在Frontier Math等研究性测试集中表现较差。随着模型换代，思维链模型应该能逐步用于辅助技术、教育和医疗等工作，但短期内还不能完全代替人工。")

st.subheader("2.小参数量模型", divider=True)
st.write("小参数量模型的需求至少在硬件性价比没有大幅提升前是一直存在的。进行数据处理、对大参数量模型进行知识蒸馏和合成思维链数据等方法可以提高小参数量模型的效果。")
st.write("phi4的经验表明，14b参数量可以达到接近旗舰模型的水平。Deepseek R1的蒸馏模型也说明目前小型化进度在14b到32b参数量之间，但考虑best of 64测试，能提高可靠性的话以后7b参数量模型有望接近旗舰模型水平。")
st.write("根据在测试集的表现，至少短期内3b和7b参数量都是不少常规任务的门槛，不过由于支持3b模型的手机和支持7b模型的电脑普及率足够高，这一问题影响不大。")

st.subheader("3.多模态模型和世界模型", divider=True)
st.write("从广义上看使用实拍等形式创建的数据训练的模型一般可以称为世界模型，包括视频生成模型、3D生成模型、交互式视频模型和端到端无人驾驶模型等。由于这类数据比较丰富，模型也有较大提升空间。")
st.write("多模态模型，使用更多实拍数据训练也有助于提高空间理解等能力从而适用于规划等任务；交互式视频模型能力提升有望突破实时渲染效果瓶颈，改变游戏等应用的形式；机器人和无人驾驶方面的端到端模型有望扩展任务范围，降低开发成本。")

st.subheader("4.绘画模型/视频模型的参数量和成本", divider=True)
