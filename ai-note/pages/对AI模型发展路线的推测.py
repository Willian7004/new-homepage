st.write("对于视频模型，Wan2.1 14b动态物体画质较好，与更大参数量的Step Video接近，因此对动态物体画质要求高的话可能需要14b左右的模型。")
st.write("在主流配置下，Hunyuan Video用于批量文生图时较慢，轻度使用速度没问题。Wan2.1 1.3b在主流配置速度还可以，更大的模型生成长度和速度都不理想。Hunyuan Video在Siliconflow的定价是每个视频2.8元，非商用价格还是偏高了，推测Step Video和Wan2.1 14b成本也接近。较新的商业模型有一些也考虑了成本问题，效果好且成本相对较低的可灵1.6定价按订阅价格换算约为每秒0.4元，而效果更好的Veo2价格为每秒3.5元，更偏向商业应用。")

